#2. (Muito Fácil) Imprimir números de 1 a 50 (Ordem Decrescente)
#Desenvolva um programa que imprima todos os números inteiros de 50 a 1 em ordem decrescente.

i = 50  # Inicializa o contador com 50
while i >= 1:  # Enquanto o contador for maior ou igual a 1, executa o loop
    print(i)  # Imprime o valor atual de i
    i -= 1  # Decrementa o valor de i em 1
