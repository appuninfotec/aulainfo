#1. (Muito Fácil) Imprimir números de 1 a 50 (Ordem Crescente)
#Desenvolva um programa que imprima todos os números inteiros de 1 a 50 em ordem crescente.


i = 1  # Inicializa o contador com 1
while i <= 50:  # Enquanto o contador for menor ou igual a 50, executa o loop
    print(i)  # Imprime o valor atual de i
    i += 1  # Incrementa o valor de i em 1
